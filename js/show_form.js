function show_form_desc() {
  let x = document.getElementsByClassName("form_desc");
  for (i = 0; i < x.length; i++) {
    x[i].style.display = "block";
  }
}
function show_form_img() {
  let x = document.getElementsByClassName("form_img");
  for (i = 0; i < x.length; i++) {
    x[i].style.display = "block";
  }
}
